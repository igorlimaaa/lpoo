    private void jButtonPesIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesIdActionPerformed
        // TODO add your handling code here:try {
        try{
            modeloCliente cliente = new modeloCliente();
            cliente.setId(Integer.parseInt(campoid.getText()));
            ConexaoBD conexao = new ConexaoBD();
            cliente = conexao.ProcurarCliente(cliente.getId());
        
            if (cliente.getId() != 0) {
                campoNome.setText(cliente.getNome());
                campoDataDeNascimento.setText(cliente.getDatadenascimento());
                campoRG.setText(cliente.getRg());
                campoOrgao.setText(cliente.getOrgao());
                campoCPF.setText(cliente.getCpf());
                campoEmail.setText(cliente.getEmail());
                campoTelefone.setText(cliente.getTelefone());
                campoCEP.setText(cliente.getCep());
                campoCidade.setText(cliente.getCidade());
                campoBairro.setText(cliente.getBairro());
                campoRua.setText(cliente.getRua());
                campoNumero.setText(cliente.getNumero());
                campoComplemento.setText(cliente.getComplemento());
                jButtonSalvar.setEnabled(true);
                jButtonCancelar.setEnabled(true);
                jButtonExcluir.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Cliente Não Encontrado", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
            } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro no banco de dados" + ex.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesIdActionPerformed

