    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try{
            int id = Integer.parseInt(campoid.getText());
            String nome = campoNome.getText();
            String datadenascimento = campoDataDeNascimento.getText();
            String rg = campoRG.getText();
            String orgao = campoOrgao.getText();
            String cpf = campoCPF.getText();
            String email = campoEmail.getText();
            String telefone = campoTelefone.getText();
            String cep = campoCEP.getText();
            String cidade = campoCidade.getText();
            String bairro = campoBairro.getText();
            String rua = campoRua.getText();
            String numero = campoNumero.getText();
            String complemento = campoComplemento.getText();
            String uf = campoUF.getSelectedItem().toString();
            modeloCliente clientes = new modeloCliente(id, nome, datadenascimento, rg, orgao, cpf, email, telefone, cep, uf, cidade, bairro, rua, numero, complemento);
            ConexaoBD conecta_cliente = new ConexaoBD();
            conecta_cliente.AtualizarCliente(clientes, id);
            JOptionPane.showMessageDialog(null, "Dados gravados com sucesso!", "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
            campoid.setText("");
            campoNome.setText("");
            campoDataDeNascimento.setText("");
            campoRG.setText("");
            campoOrgao.setText("");
            campoCPF.setText("");
            campoEmail.setText("");
            campoTelefone.setText("");
            campoCEP.setText("");
            campoCidade.setText("");
            campoBairro.setText("");
            campoRua.setText("");
            campoNumero.setText("");
            campoComplemento.setText("");
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Erro no banco de dados!"+ex.getMessage(),"Aviso",
	    JOptionPane.ERROR_MESSAGE);
			ex.printStackTrace();
        }
            
    }//GEN-LAST:event_jButtonSalvarActionPerformed

